#include <iostream>
#include <string>

int main()
{
    float a, b, c, d, e;
    a = 2;
    b = 4;
    c = a + b;
    d = a / b;
    e = a * b;
    std::cout << "a = " << a << std::endl;
    std::cout << "b = " << b << std::endl;
    std::cout << "c = a + b = " << c << std::endl;
    std::cout << "d = a / b = " << d << std::endl;
    std::cout << "e = a * b = " << e << std::endl;

    std::cout << "____________\n";
}